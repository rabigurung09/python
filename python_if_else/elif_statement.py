"""
=========elif statement=======

elif statement is use by using elif keyword
elif statement alows to check multiple expression for true and execute a block of code as 
soon as the condition is meet or true.




"""
a=30
b=50
if a>b:
    print("a is greater than b")
elif a==b:
    print("a is equal to b")
elif a<=b:
    print("a is less than or equal to b")
# output:a is less than or equal to b
